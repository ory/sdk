        /// <summary>
        /// Configures the HttpClients.
        /// </summary>
        /// <param name="client"></param>
        /// <param name="builder"></param>
        /// <returns></returns>
        public HostConfiguration AddApiHttpClients
        (
            Action<HttpClient>? client = null, Action<IHttpClientBuilder>? builder = null)
        {
            if (client == null)
                client = c => c.BaseAddress = new Uri(ClientUtils.BASE_ADDRESS);

            List<IHttpClientBuilder> builders = new List<IHttpClientBuilder>();

            builders.Add(_services.AddHttpClient<ICourierApi, CourierApi>(client));
            builders.Add(_services.AddHttpClient<IEventsApi, EventsApi>(client));
            builders.Add(_services.AddHttpClient<IFrontendApi, FrontendApi>(client));
            builders.Add(_services.AddHttpClient<IIdentityApi, IdentityApi>(client));
            builders.Add(_services.AddHttpClient<IJwkApi, JwkApi>(client));
            builders.Add(_services.AddHttpClient<IMetadataApi, MetadataApi>(client));
            builders.Add(_services.AddHttpClient<IOAuth2Api, OAuth2Api>(client));
            builders.Add(_services.AddHttpClient<IOidcApi, OidcApi>(client));
            builders.Add(_services.AddHttpClient<IPermissionApi, PermissionApi>(client));
            builders.Add(_services.AddHttpClient<IProjectApi, ProjectApi>(client));
            builders.Add(_services.AddHttpClient<IRelationshipApi, RelationshipApi>(client));
            builders.Add(_services.AddHttpClient<IWellknownApi, WellknownApi>(client));
            builders.Add(_services.AddHttpClient<IWorkspaceApi, WorkspaceApi>(client));
            
            if (builder != null)
                foreach (IHttpClientBuilder instance in builders)
                    builder(instance);

            HttpClientsAdded = true;

            return this;
        }

